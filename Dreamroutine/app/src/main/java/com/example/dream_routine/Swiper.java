package com.example.dream_routine;

public class Swiper {
    String job,task;

    public Swiper(String job, String task) {
        this.job = job;
        this.task = task;
    }

    public String getJob() {
        return job;
    }

    public String getTask() {
        return task;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public void setTask(String task) {
        this.task = task;
    }
}
